package org.jacob.leetcode.java.common;

/**
 * Definition for singly-linked list.
 *
 * @author LeetCode
 */
public class ListNode {

    public int val;
    public ListNode next;

    public ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}